    private void CarregaDoces() {
        int i = 0;
        String sql = "select * from tb01_produto";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                if(rs.getInt("tb01_qtde")>0){
                Image img = Toolkit.getDefaultToolkit().createImage(rs.getBytes("tb01_foto"));
                ImageIcon foto = new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(Fotoproduto[i].getWidth(), Fotoproduto[i].getHeight(), Image.SCALE_DEFAULT));
                Fotoproduto[i].setIcon(foto);
                valor[i] = parseDouble(rs.getString("tb01_preco"));   
                preco = rs.getString("tb01_preco");
                Precoproduto[i].setText(formatador.ConvMoeda(preco));
                }else{
                botao[i].setEnabled(false);
                }
                i++;
            }
        } catch (SQLException e) {
            System.out.println("Erro" + e);
        }
    }

    private void movimento(final JLabel fig) {
        new Thread() {
            @Override
            public void run() {
                int y = fig.getLocation().y;
                int x = fig.getLocation().x;
                int z = lblQuadroSaida.getLocation().y;

                while (y <= z) {
                    y = y + 1;
                    fig.setLocation(x, y);

                    try {
                        sleep(6);
                    } catch (InterruptedException e) {
                        System.out.println("erro" + e);
                    }
                }
                lblQuadroSaida.setIcon(fig.getIcon());
                fig.setIcon(null);
            }

        }.start();
    }

